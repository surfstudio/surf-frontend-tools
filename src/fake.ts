export const withPagination = <
  Data extends Array<any> | string,
  Params extends { page: any; size: any }
>(
  data: Data,
  params: Params
) => {
  const { page, size } = params,
    res: Res = {};

  res.content = data.slice((page - 1) * size, size);
  res.pageable = {
    sort: {
      sorted: false,
      unsorted: true,
      empty: res.content.length < 1,
    },
    pageNumber: page,
    pageSize: Math.min(size, res.content.length),
    offset: page * size,
    paged: true,
    unpaged: false,
  };

  res.sort = {
    sorted: false,
    unsorted: true,
    empty: res.content.length < 1,
  };

  res.totalPages = Math.ceil(data.length / size);
  res.totalElements = data.length;
  res.last = res.totalPages === page * size;

  res.number = page;
  res.numberOfElements = res.content.length;
  res.first = page === 1;
  res.size = size;
  res.empty = res.content.length < 1;

  return res;
};

